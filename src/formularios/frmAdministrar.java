    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        final String usuarioABorrar = String.valueOf(jComboBox1.getSelectedItem());
        if (!usuarioABorrar.equals("null")) {
            String contrasenia = JOptionPane.showInputDialog(this, "Ingresar contraseña para el usuario: " + usuarioABorrar);
            for (int i = 0; i < listaDeUsuario.size(); ++i) {
                if (listaDeUsuario.get(i).getUsuario().equals(usuarioABorrar)) {
                    if (listaDeUsuario.get(i).getContrasenia().equals(contrasenia)) {
                        CP.eliminarDatos(usuarioABorrar);
                        JOptionPane.showMessageDialog(this, String.format("El usuario: %s ha sido eliminado", usuarioABorrar));
                        listaDeUsuario.remove(i);
                        break;
                    }
                    JOptionPane.showMessageDialog(this, "Contraseña incorrecta, tiene que ingresar la contraseña para eliminar el usuario", "Error", 0);
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "No hay mas usuarios para eliminar", "Error", 0);
        }
        actualizarComboBox();
    }//GEN-LAST:event_btnBorrarActionPerformed
    
    public void actualizarComboBox() {
        listaDeUsuario = CP.getDatos();
        String[] listaNombreDeUsuario = new String[listaDeUsuario.size()];
        for (int i = 0; i < listaNombreDeUsuario.length; ++i) {
            listaNombreDeUsuario[i] = listaDeUsuario.get(i).getUsuario();
        }
        modelo = new DefaultComboBoxModel<>(listaNombreDeUsuario);
        jComboBox1.setModel(modelo);
    }
